public class Loops {
    public static void main(String[] args) {
        // while loops
        int a = 1;
        while(a < 10) {
            System.out.format("Current number is %d\n", a);
            a += 1;
        }

        // for loops
        for(int i=0; i<5; i++) {
            System.out.println(i);
        }
    }
}
